/**
 * Enum representing different types of chats in Telegram.
 * @description Contains different types of chats in Telegram
 * PRIVATE - Represents a private chat between two users
 * GROUP - Represents a group chat
 * SUPERGROUP - Represents a supergroup chat
 * CHANNEL - Represents a channel chat
 */
export enum ChatType {
  PRIVATE = "PRIVATE",
  GROUP = "GROUP",
  SUPERGROUP = "SUPERGROUP",
  CHANNEL = "CHANNEL",
}
